"use client";

import { motion } from "framer-motion";
import { ReactNode } from "react";

/**
 * One subtle entrance reveal. Transform is auto-disabled under reduced motion
 * by the app-level MotionConfig; opacity still fades.
 */
export function Reveal({
  children,
  delay = 0,
  className = "",
}: {
  children: ReactNode;
  delay?: number;
  className?: string;
}) {
  return (
    <motion.div
      initial={{ opacity: 0, y: 8 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.5, delay, ease: [0.22, 1, 0.36, 1] }}
      className={className}
    >
      {children}
    </motion.div>
  );
}
